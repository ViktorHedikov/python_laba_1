h1, m1, s1 = map(int, input().split())
h2, m2, s2 = map(int, input().split())
# переводим оба времени в секунды
t1 = h1*3600 + m1*60 + s1
t2 = h2*3600 + m2*60 + s2
# находим разницу по модулю
t3 = abs(t2 - t1)
#вычисляем часы и минуты
h3 = t3//3600
m3 = t3 % 3600
#проверка минут и вычисление секунд
if m3 <= 60:
    s3 = m3
    m3 = 0
else:
    s3 = m3 % 60
    m3 = m3 // 60
print(h3, m3, s3)